package Fundamentos;

public class operadoresUnarios {
	
	public static void main(String[] args) {
		int num1 = 1;
		int num2 = 2;
		
		//incremento e decremento
		num1++; // elemento pos ficsado
		System.out.println(num1);
		--num1; // elemento pre ficsado 
		System.out.println(num1);
		System.out.println(num2);
	}

}
